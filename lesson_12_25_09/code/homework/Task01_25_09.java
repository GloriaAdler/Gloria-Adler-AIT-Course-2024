//–ó–∞–¥–∞—á–∞ 1. –°–æ—Å—Ç–∞–≤—å—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã—á–∏—Å–ª—è–µ—Ç —Å—É–º–º—É —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 1/n, n –≤–≤–æ–¥–∏—Ç—Å—è —Å –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã.
//–ü—Ä–∏–º–µ—Ä: n = 10 sum = 1 + 1/2 + 1/3 + ... + 1/10 –ò—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Ü–∏–∫–ª for.
import java.util.Scanner;
public class Task01_25_09 {
    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);
        System.out.print("Input n: ");
        int n = scanner.nextInt();//–∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –≤–≤–æ–¥ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è

        double sum = 0.0;//–ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å—É–º–º—ã, double - —Ç–∞–∫ –∫–∞–∫ –¥—Ä–æ–±–∏
//–∏—Å–ø–æ–ª—å–∑—É–µ–º —Ü–∏–∫–ª for, —á—Ç–æ–±—ã –≤—ã—á–∏—Å–ª–∏—Ç—å —Å—É–º–º—É —á–∏—Å–µ–ª –≤–∏–¥–∞ 1 + 1/2 + 1/3 +...+ 1/n-–≤–≤–µ–¥–µ–Ω–Ω–æ–µ —á–µ—Ä–µ–∑ —Å–∫–∞–Ω
        for (int i = 1; i <= n; i++) { // –ò—Å–ø–æ–ª—å–∑—É–µ–º —Ü–∏–∫–ª for –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è —Å—É–º–º—ã 1 + 1/2 + ... + 1/n. –¶–∏–∫–ª –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å 1 –∏ –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è –¥–æ –∑–Ω–∞—á–µ–Ω–∏—è n (–≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ).

            sum += 1.0 / i; // –ü—Ä–∏–±–∞–≤–ª—è–µ–º 1 –¥–µ–ª–µ–Ω–Ω–æ–µ i –∫ —Å—É–º–º–µ. –ù–∞ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –æ–ø–µ—Ä–∞—Ü–∏—è 1/ùëñ. –≥–¥–µ i ‚Äî —Ç–µ–∫—É—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Ü–∏–∫–ª–∞, –∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è –∫ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π sum.

        }//end of for

        System.out.println("Sum " + n + " = " + sum);// –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    }//end of main
}//end of class

